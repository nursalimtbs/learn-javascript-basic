// logical operators example
let a = true;
let b = false;

console.log(a && b); // false
console.log(a || b); // true
console.log(!a); // false
console.log(!b); // true

let x = 10;
let y = 5;

console.log((x > 5) && (y < 10)); // true
console.log((x < 5) || (y < 10)); // true
console.log(!(x === 10)); // false  
console.log(!(y !== 5)); // true
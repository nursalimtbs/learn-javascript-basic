// using var keyword

// create a variable
var fullName;

// add value to the variable
fullName = "Nursalim";

console.info(fullName);

// variable with value assigned directly
var courseName = "Learn JavaScript Basics";
console.info(courseName);

// using let keyword
let age = 25;
console.info(age);

// using const keyword
const country = "Indonesia";
console.info(country);